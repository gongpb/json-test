package com.gongpb.ad.vo;

import java.util.List;

public class SeedVo {
	private List sus;
	private List sue;
	private int vt;
	private String vc;
	public List getSus() {
		return sus;
	}
	public void setSus(List sus) {
		this.sus = sus;
	}
	public List getSue() {
		return sue;
	}
	public void setSue(List sue) {
		this.sue = sue;
	}
	public int getVt() {
		return vt;
	}
	public void setVt(int vt) {
		this.vt = vt;
	}
	public String getVc() {
		return vc;
	}
	public void setVc(String vc) {
		this.vc = vc;
	}
	
}
